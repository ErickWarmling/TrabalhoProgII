    private void jBPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBPesquisarActionPerformed
       String numeroVoo = jTextVoo.getText();
      
      try{ 
       if (!numeroVoo.isEmpty()){
           DefaultTableModel modelo = (DefaultTableModel)jTablePassageirosVoo.getModel();
           modelo.setRowCount(0);
           PassageiroController ctc = new PassageiroController();
            List<Passageiro> passageirosVoo = ctc.consultarPassageiroPorVoo(numeroVoo);
            for (Passageiro passageiro : passageirosVoo){
                modelo.addRow(new String[]{
                    String.valueOf(passageiro.getId()),
                            passageiro.getNome(),
                            String.valueOf(passageiro.getIdade()),
                            passageiro.getCpf(),
                            passageiro.getVoo(),
                            passageiro.getDestino(),
                            passageiro.getClasse(),
                            String.valueOf(passageiro.getPoltrona())
            });
        }
       }
      }  catch (Exception e){
                    JOptionPane.showMessageDialog(rootPane, e.getMessage());
    }
    }//GEN-LAST:event_jBPesquisarActionPerformed

    private void jBExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBExcluirActionPerformed
        try {
            PassageiroController ctc = new PassageiroController();
            int linha = jTablePassageirosVoo.getSelectedRow();
            int id = Integer.parseInt(jTablePassageirosVoo.getValueAt(linha, 0).toString());
            ctc.excluir(id);
            
            DefaultTableModel modelo = (DefaultTableModel)jTablePassageirosVoo.getModel();
            modelo.removeRow(linha);
            JOptionPane.showMessageDialog(rootPane, "Passageiro exclu√≠do com sucesso");
            
        }catch (RuntimeException e) {
            JOptionPane.showMessageDialog(rootPane, "Selecione um passageiro para excluir");
        }
    }//GEN-LAST:event_jBExcluirActionPerformed

    private void jBAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBAtualizarActionPerformed
        try {
            int linha = jTablePassageirosVoo.getSelectedRow();
            int id = Integer.parseInt(jTablePassageirosVoo.getValueAt(linha, 0).toString());
            String nome = jTablePassageirosVoo.getValueAt(linha, 1).toString();
            int idade = Integer.parseInt(jTablePassageirosVoo.getValueAt(linha, 2).toString());
            String cpf = jTablePassageirosVoo.getValueAt(linha, 3).toString();
            String voo = jTablePassageirosVoo.getValueAt(linha, 4).toString();
            String destino = jTablePassageirosVoo.getValueAt(linha, 5).toString();
            String classe = jTablePassageirosVoo.getValueAt(linha, 6).toString();
            int poltrona = Integer.parseInt(jTablePassageirosVoo.getValueAt(linha, 7).toString());
            
            Passageiro passageiro = new Passageiro(nome, idade, cpf, voo, destino, classe, poltrona, id);
            new FrameAtualizarPassageiro(passageiro).setVisible(true);
            this.dispose();
            
        } catch (RuntimeException e) {
            JOptionPane.showMessageDialog(rootPane, "Selecione um passageiro para atualizar");
        }
    }//GEN-LAST:event_jBAtualizarActionPerformed

