    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        DefaultTableModel modelo = (DefaultTableModel)jTableAeroportos.getModel();
        
        AeroportoController ctc = new AeroportoController();
        List<Aeroporto> aeroportos = ctc.consultarTodos();
        for (Aeroporto aeroporto : aeroportos){
            modelo.addRow(new String[]{
                String.valueOf(aeroporto.getId()),
                        aeroporto.getNome(),
                        aeroporto.getCidade(),
                        aeroporto.getEstado(),
                        aeroporto.getPais()
            });
                
        }
    }//GEN-LAST:event_formWindowOpened

    private void jBExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBExcluirActionPerformed
        try {
            AeroportoController ctc = new AeroportoController();
            int linha = jTableAeroportos.getSelectedRow();
            int id = Integer.parseInt(jTableAeroportos.getValueAt(linha, 0).toString());
            ctc.excluir(id);
            
            DefaultTableModel modelo = (DefaultTableModel)jTableAeroportos.getModel();
            modelo.removeRow(linha);
            JOptionPane.showMessageDialog(rootPane, "Aeroporto excluído com sucesso");
            
        }catch(RuntimeException e){
            JOptionPane.showMessageDialog(rootPane, "Selecione um aeroporto para excluir");
        }
    }//GEN-LAST:event_jBExcluirActionPerformed

