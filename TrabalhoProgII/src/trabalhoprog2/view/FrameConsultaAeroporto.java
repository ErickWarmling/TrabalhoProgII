    private void jBAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBAtualizarActionPerformed
        try {   
           int linha = jTableAeroportos.getSelectedRow();
           int id = Integer.parseInt(jTableAeroportos.getValueAt(linha, 0).toString());
           String nome = jTableAeroportos.getValueAt(linha, 1).toString();
           String cidade = jTableAeroportos.getValueAt(linha, 2).toString();
           String estado = jTableAeroportos.getValueAt(linha, 3).toString();
           String pais = jTableAeroportos.getValueAt(linha, 4).toString();
           
           Aeroporto aeroporto = new Aeroporto (nome, cidade, estado, pais, id);
           new FrameAtualizarAeroporto(aeroporto).setVisible(true);
           this.dispose();
        }catch (RuntimeException e){
            JOptionPane.showMessageDialog(rootPane, "Selecione um aeroporto para atualizar");
        }
    }//GEN-LAST:event_jBAtualizarActionPerformed

